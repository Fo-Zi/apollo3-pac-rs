#[doc = "Serial ISO7816."]
#[derive(Copy, Clone, Eq, PartialEq)]
pub struct Scard {
    ptr: *mut u8,
}
unsafe impl Send for Scard {}
unsafe impl Sync for Scard {}
impl Scard {
    #[inline(always)]
    pub const unsafe fn from_ptr(ptr: *mut ()) -> Self {
        Self { ptr: ptr as _ }
    }
    #[inline(always)]
    pub const fn as_ptr(&self) -> *mut () {
        self.ptr as _
    }
    #[doc = "ISO7816 interrupt status."]
    #[inline(always)]
    pub const fn sr(self) -> crate::common::Reg<regs::Sr, crate::common::RW> {
        unsafe { crate::common::Reg::from_ptr(self.ptr.wrapping_add(0x0usize) as _) }
    }
    #[doc = "ISO7816 data."]
    #[inline(always)]
    pub const fn dr(self) -> crate::common::Reg<regs::Dr, crate::common::RW> {
        unsafe { crate::common::Reg::from_ptr(self.ptr.wrapping_add(0x10usize) as _) }
    }
    #[doc = "ISO7816 interrupt status 1."]
    #[inline(always)]
    pub const fn sr1(self) -> crate::common::Reg<regs::Sr1, crate::common::RW> {
        unsafe { crate::common::Reg::from_ptr(self.ptr.wrapping_add(0x20usize) as _) }
    }
    #[doc = "ISO7816 resent count inquiry."]
    #[inline(always)]
    pub const fn retxcntrmi(self) -> crate::common::Reg<regs::Retxcntrmi, crate::common::RW> {
        unsafe { crate::common::Reg::from_ptr(self.ptr.wrapping_add(0x38usize) as _) }
    }
    #[doc = "Clock Control."]
    #[inline(always)]
    pub const fn clkctrl(self) -> crate::common::Reg<regs::Clkctrl, crate::common::RW> {
        unsafe { crate::common::Reg::from_ptr(self.ptr.wrapping_add(0x0100usize) as _) }
    }
}
pub mod regs;
